package mol.model.curso.atividade.analiseDesempenho;

public class Ticks {
	
	private Boolean beginAtZero;
	private double max;
	private int maxTicksLimit;
	
	
	
	public Boolean getBeginAtZero() {
		return beginAtZero;
	}
	public void setBeginAtZero(Boolean beginAtZero) {
		this.beginAtZero = beginAtZero;
	}
	public double getMax() {
		return max;
	}
	public void setMax(double d) {
		this.max = d;
	}
	public int getMaxTicksLimit() {
		return maxTicksLimit;
	}
	public void setMaxTicksLimit(int maxTicksLimit) {
		this.maxTicksLimit = maxTicksLimit;
	}
	
}